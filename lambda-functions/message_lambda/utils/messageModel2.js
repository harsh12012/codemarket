const mongoose = require('mongoose');

const message2Schema = new mongoose.Schema({
  listingLocation: {
    type: String,
    required: true,
  },
  senderId: {
    type: String,
    required: true,
  },
  senderName: {
    type: String,
    required: true,
  },
  receiverId: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now(),
  },
});

const Message2 = mongoose.model('Message2', message2Schema);

module.exports = Message2;
